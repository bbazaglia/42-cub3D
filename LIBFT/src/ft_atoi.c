int	ft_atoi(const char *nptr)
{
	int	sign;
	int	sum;

	sign = 1;
	sum = 0;
	while ((*nptr >= '\t' && *nptr <= '\r') || *nptr == ' ')
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign *= -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		sum = sum * 10 + *nptr - '0';
		nptr++;
	}
	return (sum * sign);
}
